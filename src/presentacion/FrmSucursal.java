    private void limpiarTextos() {
        this.txtCodigo.setText(" ");
        this.txtCiudad.setText(" ");
        this.txtDireccion.setText(" ");
        this.txtNombre.setText(" ");
    }
    
    private void activar(Boolean flag) {
        this.btnNuevo.setEnabled(!flag);
        this.btnRegistrar.setEnabled(flag);
        this.btnSalir.setEnabled(flag);
        
    }

