    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        this.codigo = this.txtCodigo.getText().trim();
        this.nombre = this.txtNombre.getText();
        this.direccion = this.txtDireccion.getText();
        this.ciudad = this.txtCiudad.getText();
        this.mensaje = SucursalBL.insertarSucursal(codigo, nombre, ciudad, direccion);
        if (this.mensaje.compareTo("true") == 0) {
            JOptionPane.showMessageDialog(this,"Inserted data", "Resultado", 1);
             this.limpiarTextos();
        } else {
            JOptionPane.showMessageDialog(this, this.mensaje, "Resultado", 1);
        }
       
        this.activar(true);
    }//GEN-LAST:event_btnRegistrarActionPerformed

